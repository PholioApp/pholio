import { serve } from "https://deno.land/std@0.190.0/http/server.ts";
import Stripe from "https://esm.sh/stripe@18.5.0";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.57.2";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  const supabaseClient = createClient(
    Deno.env.get("SUPABASE_URL") ?? "",
    Deno.env.get("SUPABASE_ANON_KEY") ?? ""
  );

  try {
    console.log("Starting ad checkout process");
    
    const authHeader = req.headers.get("Authorization")!;
    const token = authHeader.replace("Bearer ", "");
    const { data } = await supabaseClient.auth.getUser(token);
    const user = data.user;
    
    if (!user?.email) {
      throw new Error("User not authenticated");
    }

    console.log("User authenticated:", user.id);

    const { siteUrl, title, description, imageUrl, days } = await req.json();
    
    if (!siteUrl || !title || !days) {
      throw new Error("Site URL, title, and days are required");
    }

    console.log("Ad request:", { siteUrl, title, days });

    // Free for nipsubroder@gmail.com, $0.10 per day for others
    const isFreeUser = user.email === "nipsubroder@gmail.com";
    const amount = isFreeUser ? 0 : days * 0.10;
    
    const stripe = new Stripe(Deno.env.get("STRIPE_SECRET_KEY") || "", {
      apiVersion: "2025-08-27.basil",
    });

    // Check for existing customer
    const customers = await stripe.customers.list({ email: user.email, limit: 1 });
    let customerId;
    if (customers.data.length > 0) {
      customerId = customers.data[0].id;
    }

    console.log("Creating Stripe checkout session");

    // Skip payment if free
    if (isFreeUser) {
      // Create ad directly without payment
      const { data: adData, error: adError } = await supabaseClient
        .from('ads')
        .insert({
          advertiser_id: user.id,
          site_url: siteUrl,
          title: title,
          description: description || "",
          image_url: imageUrl || "",
          amount_paid: 0,
          start_date: new Date().toISOString(),
          end_date: new Date(Date.now() + days * 24 * 60 * 60 * 1000).toISOString(),
          status: 'active'
        })
        .select()
        .single();

      if (adError) throw adError;

      return new Response(JSON.stringify({ success: true, ad: adData }), {
        headers: { ...corsHeaders, "Content-Type": "application/json" },
        status: 200,
      });
    }

    // Create checkout session for ad
    const session = await stripe.checkout.sessions.create({
      customer: customerId,
      customer_email: customerId ? undefined : user.email,
      line_items: [
        {
          price_data: {
            currency: "usd",
            product_data: {
              name: `Advertise "${title}" for ${days} day${days > 1 ? 's' : ''}`,
              description: `Promote your site: ${siteUrl}`,
              images: imageUrl ? [imageUrl] : undefined,
            },
            unit_amount: amount * 100, // Convert to cents
          },
          quantity: 1,
        },
      ],
      mode: "payment",
      success_url: `${req.headers.get("origin")}/payment-success?session_id={CHECKOUT_SESSION_ID}&type=ad`,
      cancel_url: `${req.headers.get("origin")}/ads`,
      metadata: {
        type: "ad",
        advertiser_id: user.id,
        site_url: siteUrl,
        title: title,
        description: description || "",
        image_url: imageUrl || "",
        days: days.toString(),
        amount: amount.toString(),
      },
    });

    console.log("Checkout session created:", session.id);

    return new Response(JSON.stringify({ url: session.url }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
      status: 200,
    });
  } catch (error) {
    console.error("Error creating ad checkout:", error);
    const errorMessage = error instanceof Error ? error.message : "An error occurred";
    return new Response(JSON.stringify({ error: errorMessage }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
      status: 500,
    });
  }
});
